export const ALL_DISEASES = Object.keys(DISEASE_RELATED_SPECIALTIES);

export const getDiseaseInfo = (slug: string) => {
  // 1. Try exact match from constants by looking at slugified keys
  let name = Object.keys(DISEASE_RELATED_SPECIALTIES).find(k => slugify(k) === slug);
  
  // 2. Fallback if not found: convert slug to readable title case (e.g. dolor-de-espalda -> Dolor De Espalda)
  if (!name) {
    name = slug.split('-').map(w => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  }

  const relatedSpecialties = DISEASE_RELATED_SPECIALTIES[name] || [];
  const primarySpecialty = relatedSpecialties.length > 0 ? relatedSpecialties[0] : null;
  const details = DISEASE_DETAILS[name] || DISEASE_DETAILS['General'];

  return {
    name,
    primarySpecialty,
    relatedSpecialties,
    details
  };
};
